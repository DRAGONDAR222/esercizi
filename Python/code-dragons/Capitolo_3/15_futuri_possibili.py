'''Davanti a un set di rune, calcola quante combinazioni di potere sono possibili. Pronuncia `powerset_size(n)`: restituisci il numero di sottoinsiemi di un insieme con `n` elementi. Mantieni la firma e lascia che i test registrino l'ampiezza del possibile.'''

def powerset_size(n: int) -> int:

    return 2**n